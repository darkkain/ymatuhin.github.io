function photogalery()  
{  
	var button = document.getElementById("photo");  
	var galary = document.getElementById("image-group");  
	button.style.display = 'none'; 
	galary.style.display = 'block'; 
} 
function  bottomnav1()
{  			
	var first = document.getElementById("featured");  
	var second = document.getElementById("core");  
	var third = document.getElementById("jquerytuts");  
	first.style.display = 'block'; 
	second.style.display = 'none'; 
	third.style.display = 'none'; 
	var fb1 = document.getElementById("botnav1");  
	var fb2 = document.getElementById("botnav2");  
	var fb3 = document.getElementById("botnav3");  
	fb1.style.background = 'url("img/tab-active.png") no-repeat 50% 50%'; 
	fb2.style.background = 'url("img/tab.png") no-repeat 50% 50%'; 
	fb3.style.background = 'url("img/tab.png") no-repeat 50% 50%'; 
	fb1.style.color = '#fff'; 
	fb2.style.color = '#464646'; 
	fb3.style.color = '#464646'; 
} 	
